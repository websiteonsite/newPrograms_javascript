// operators in Javascript
// 1. Arithmetic operators
//     (+, -, *, /, %, ++, --)

let x= 10;
x++;
console.log(x);

let y = 11;
y--;
console.log(y);

// 2. Assignment operators

let a = 5;
a += 2;
console.log(a);

// 3. Comparision operators
// (<, >, <=, >=, ==, !=, ===, !==)

console.log( 5 == '5' );
console.log( 5 === '5' );
console.log(5>1);

// 4. Logical operators
// (&&, ||, !)

console.log( false && true );
console.log( true && false );
console.log( true || true );
console.log( true || false );
console.log( 0||7 );
console.log(!1);

